% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

s=get(handles.fun,'string');

d=strcat('@(t)',s);
f=str2func(d);
x0=str2num(get(handles.u,'string'));
dx0=str2num(get(handles.v,'string'));
m=str2num(get(handles.m,'string'));
b=str2num(get(handles.b,'string'));
k=str2num(get(handles.k,'string'));
tstart=str2num(get(handles.ts,'string'));
tend=str2num(get(handles.tend,'string'));
dt=0.0001;

t=tstart:dt:tend;
%y=f(t)
len=(tend-tstart)/dt;
us=zeros(1,len);
vs=zeros(1,len);
A=[-(b/m) -(k/m);1 0];
M=[(1/m);0];
UV=[dx0;x0];
us(1)=x0;
vs(1)=dx0;
for i=1:len
    UVp=A*UV+f(t(i))*M;
    UV=UV+UVp.*dt;
    vs(i+1)=UV(1);
    us(i+1)=UV(2);
end
if b==sqrt(4*m*k)
    disp("critically damped")
    s=1;
end
if b>sqrt(4*m*k)
    disp("overdamped")
    s=2;
end
if b<sqrt(4*m*k)
    disp("Underdamped")
    s=3;
end

axes(handles.axes1);
title('Position vs time')
plot(tstart:dt:tend,us,'r')

axes(handles.axes2);
title('Velocity vs time')
plot(tstart:dt:tend,vs,'b')

if s==1
    set(handles.testoutput, 'string','Critically damped');
end
if s==2
    set(handles.testoutput, 'string','Overdamped');
end
if s==3
    set(handles.testoutput, 'string','Underdamped');
end



